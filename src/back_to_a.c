#include "../inc/push_swap.h"

int		back_to_a(t_stack **a, t_stack **b, t_flags flags)
{
	int		ap;

	ap = 0;
	while (*b)
	{
		action("pa", a, b, flags);
		ap++;
	}
	return (ap);
}
