#include "../inc/util.h"
#include "../libft/libft.h"

void	ft_lstpush(t_stack **a, t_stack **b)
{
	t_stack	*temp;

	if (*b)
	{
		temp = (*b)->next;
		(*b)->next = *a;
		*a = *b;
		*b = temp;
	}
}
