#include "../inc/util.h"

void	ft_lstfree(t_stack **s)
{
	t_stack *tmp;

	while (*s)
	{
		tmp = *s;
		tmp->num = 0;
		(*s) = (*s)->next;
		free(tmp);
	}
}
