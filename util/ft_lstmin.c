#include "../inc/util.h"

int		ft_lstmin(t_stack *s)
{
	int min;

	min = 0;
	if (s)
		min = s->num;
	while (s)
	{
		if (min > s->num)
			min = s->num;
		s = s->next;
	}
	return (min);
}
